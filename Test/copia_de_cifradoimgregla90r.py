import numpy as np

def regla_90R(t0, t1):
    n = len(t0)
    t2 = np.zeros(n, dtype=np.uint8)
    for i in range(n):
        left = t1[i - 1] if i - 1 >= 0 else 0
        right = t1[i + 1] if i + 1 < n else 0
        center = t0[i]
        t2[i] = (left + right + center) % 2
    return t2

def cifrar_imagen_binaria(img_binaria, num_iteraciones):
    """
    Aplica cifrado de autómata celular por filas.
    img_binaria: matriz numpy 2D de 0s y 1s.
    num_iteraciones: número de veces que se aplicará la regla 90R.
    """
    img = img_binaria.copy()
    filas, cols = img.shape

    # Recorremos la imagen de 2 en 2 filas
    for i in range(0, filas - 1, 2):
        t0 = img[i]
        t1 = img[i+1]

        for _ in range(num_iteraciones):
            t2 = regla_90R(t0, t1)
            t0, t1 = t1, t2  # avanzar estados

        img[i] = t0
        img[i+1] = t1

    return img

def convert_rows_to_binary(img):
    rows = img.shape[0]
    binary_rows = []

    for r in range(rows):
        # para cada fila, convertimos cada pixel a 8 bits binarios y concatenamos
        bin_str_row = ''.join(format(pixel, '08b') for pixel in img[r])
        # convertimos la cadena de bits a lista de ints (0/1)
        bin_list = [int(bit) for bit in bin_str_row]
        binary_rows.append(bin_list)

    # convertimos la lista de listas a un np.array 2D
    return np.array(binary_rows, dtype=np.uint8)

def binary_to_pixel_array(binary_img):
    rows, total_bits = binary_img.shape
    # Cada pixel ocupa 8 bits
    num_pixels_per_row = total_bits // 8

    pixel_rows = []
    for r in range(rows):
        row_bits = binary_img[r]
        pixels = []
        for i in range(num_pixels_per_row):
            # Extraer 8 bits por pixel
            byte_bits = row_bits[i*8:(i+1)*8]
            # Convertir bits a string
            byte_str = ''.join(str(b) for b in byte_bits)
            # Convertir string binario a entero decimal
            pixel_val = int(byte_str, 2)
            pixels.append(pixel_val)
        pixel_rows.append(pixels)

    return np.array(pixel_rows, dtype=np.uint8)

