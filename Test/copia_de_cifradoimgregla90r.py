import numpy as np

def regla_90R_inversa(t1, t2):
    """
    Dada t1 y t2, obtiene t0 aplicando la regla inversa.
    """
    n = len(t1)
    t0 = np.zeros(n, dtype=np.uint8)
    for i in range(n):
        left = t1[i - 1] if i - 1 >= 0 else 0
        right = t1[i + 1] if i + 1 < n else 0
        future = t2[i]
        t0[i] = (left + right + future) % 2
    return t0

def descifrar_imagen_binaria(img_cifrada, num_iteraciones):
    """
    Descifra la imagen binaria cifrada con la Regla 90R.

    img_cifrada: numpy 2D con la imagen cifrada (0s y 1s)
    num_iteraciones: número de iteraciones que se usaron para cifrar

    Retorna la imagen descifrada.
    """
    img = img_cifrada.copy()
    filas, cols = img.shape

    for i in range(0, filas - 1, 2):
        # Estado en t+1 y t+2 al final del cifrado (últimas dos filas)
        t2 = img[i+1]
        t1 = img[i]

        # Iterar hacia atrás para recuperar t0
        for _ in range(num_iteraciones):
            t0 = regla_90R_inversa(t1, t2)
            t2, t1 = t1, t0  # retroceder estados

        img[i] = t1
        img[i+1] = t2

    return img

